 //  The BigInt (Data Type) can store and operate on large integers even beyond the integers specific limit i.e: (safe integer limit)..

 //  A BigInt is created by appending (n) to the end of an integer or by calling the BigInt() function.

 //  Example:
 
 //  By appending (n).

 const x = 2n;
 console.log(x);

 //  Output:
 //  2n


 //  By calling BigInt() function.

 const y = BigInt(2);
 console.log(y);

 //  Output:
 //  2n

