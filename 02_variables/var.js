// The (var) keyword is used to declare variables in JavaScript.

// Syntax:
// var (variableName) = (variableValue);

// Example:

var FirstName = "John";
console.log(FirstName);

// Output:
// John

// There are three phases of variables.

// 1) Declaration.

// Example:
var LastName;

// 2) Initialization.

// Example:
LastName = "Doe";

// 3) Usage.

// Example:
console.log(LastName); 

// Output:
// Doe

