 //  PRIMITIVE VALUES...
 
 
 //  All types except object define immutable values represented directly at the lowest level of language, we refer to values of these types as PRIMITIVE VALUES.


 //  In JavaScript, primitive values are simple, immutable data types that represent single values. These primitive data types are used to store and manipulate basic values.
 

 // We discuss about five data primitive data types in JS.


 //  Primitive values are immutable, which means that their values cannot be changed after they are created. Any operation that appears to change a primitive value actually creates a new value based on the original.


 // Example:

 var str = "Hello";
 str = str + " World"; // Creates a new string, "Hello World", and assigns it to str


