 //  The sort method sorts the elements of an array in place and returns the reference to the same array, now sorted. The default sort order is ascending, built upon converting the elements into UTF-16 code unit values.

 //  Example:

 const months = ["March","Jan","Feb","December"];
 console.log(months.sort());    //   ['December', 'Feb', 'Jan', 'March']



