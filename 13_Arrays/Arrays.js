 //  An Array is the ordered collection of data(wither primitive or object depending). Arrays are used to store multiple values in a single variable.

 //  Each item in an Array has a number attached to it, called a numeric index, that allows you to access it.

 //  Example:

 let arr = ["item1", "item2"];
 console.log(arr);  //  ["item1", "item2"]

 console.log(arr[1]);   //  item2