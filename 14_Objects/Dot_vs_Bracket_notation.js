 //  In JS Objects, you can accessed properties(values) by bracket and dot notation.

 //  Example:

 const person = {
    name:"John",
    gender:"male"
 }

 person["qualification"] = "Undergrauate";
 console.log(person);     //  {name: 'John', gender: 'male', qualification: 'Undergrauate'}

 person.qualification = "undergraduate";
 console.log(person);     //  {name: 'John', gender: 'male', qualification: 'undergraduate'}
