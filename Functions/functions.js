 //  Functions are one of the funamental building blocks in JavaScript. A function in JavaScript is similar to a procedure—a set of statements that performs a task or calculates a value, but for a procedure to qualify as a function, it should take some input and return an output where there is some obvious relationship between the input and the output. To use a function, you must define it somewhere in the scope from which you wish to call it.

 //  A)  Functions Declarations

 //  A function definition (also called a function declaration, or function statement) consists of the function keyword, followed by:

 // 1)  The name of the function.
 // 2)  A list of parameters to the function, enclosed in parentheses and separated by commas.
 // 3)  The JavaScript statements that define the function, enclosed in curly braces, { /* … */ }.

 //  Example:

 function square(number) {
    return number * number;
  }

