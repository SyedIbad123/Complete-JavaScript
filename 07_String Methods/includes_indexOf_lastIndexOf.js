 //  Includes() method...

 //  It determines whether the given string includes certain character at any index. It always return Boolean.

 //  Example:

 let college = "NED";
 console.log(college.includes("E"));

 //  Output:
 // true


 //  indexOf() method..

 //  It returns the index within the calling string object of the first occurence of search value, or -1 if not found.

 //  Example:

 console.log(college.indexOf("N"));

 //  Output:
 //   0  (because "N" occurs at 0th index)


 //  lastIndexof() method...

 //  It returns the index within the calling string object the last occurence of search value, or -1 if not found.

 //  Example:

 const paragraph = "Ali is a good boy";
 const word = "good";
 console.log(paragraph.lastIndexOf(word));

 //  Output:
 //  9
