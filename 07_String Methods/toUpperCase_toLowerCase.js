 //  toUpperCase() method...

 //  It returns string into UPPER CASE characters...

 //  Example:

 const x = "ibad";
 console.log(x.toUpperCase());

 //  Output:
 //  IBAD


 //  toLowerCase() method...

 //  It returns string into lowercase characters...

 //  Example:

 const y = "ALi";
 console.log(y.toLowerCase());

 //  Output:
 //  ali


 //  trim() method...

 //  This method removes whitespace from both ends of the string and returns a new string, without modifying the original string.

 //  To return a new string with whitespace trimmed from just one end, use trimstart() and trimend().

 //  Example:

 const myName = "    Ibad     ";
 console.log(myName.trim());

 //  Output:
 //  "Ibad"
 
 