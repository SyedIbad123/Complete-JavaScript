 //  Concat() method...

 //  This method combines two or more text to form a new string..

 //  Example:

 let firstName = "Ibad";
 let lastName = " Ali";
 console.log(firstName.concat(lastName));

 //  Output:
 //  Ibad Ali


 //  StartsWith() method...

 //  It determines whether the string starts with the character of the string.
 //  It always returns Boolean value.

 //  Example:

 console.log(firstName.startsWith("I"));

 //  Output:
 //  true


 //  endsWith() method...

 //  It determines whether the string ends with the character of the string.

 console.log(lastName.endsWith("o"));

 //  Output:
 //  false