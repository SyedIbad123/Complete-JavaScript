// The (count) method of console counts the number of times console.count() is called.

// Example:
console.count();

// Output: 
// default : 1

// Here default is the label of count we can change it by any paramter.

// Example: 
console.count("Ibad");

// Output:
// Ibad : 1


// Example:

console.count();
console.count("NED");
console.count();

// Output:

// default:2 
// NED:1 
// default:3 
