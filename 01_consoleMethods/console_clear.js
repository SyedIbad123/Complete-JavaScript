// The (clear) method of console clear all running messages or code in console..

// Example: 
console.clear();

// It also shows a message that (Console was cleared) in console.


// The (info) method of console is used to print the text to console as an informational message. It is same as (console.log()) but differ in the type of message it displays on the console.

// Example:
console.info("Software Engineering");
// This is informational message.

// Output:
// Software Engineering