 //  Arithmetic Operators...

 // Addition(+) returns the sum of two operands or string concatenation.

 console.log(2+2);  //  4
 console.log("Ibad " + "ali");  //  Ibad ali


 //  Subtraction(-) returns the difference...

 console.log(2-2); //  0


 //  Multiplication(*) returns Product...

 console.log("2" * 2); // 4


 //  Division(/) produces the quotient of its operands...

 console.log(2/0); //  Infinity


 //  Modulo(%) returns remainder let over. It always take sign of dividend...

 console.log(13%5);  //  3


 //  Exponentiation(**) results f raising first operand power to the power of the second operand.It is equal to Math.pow()...

 console.log(3**4);  //  81




