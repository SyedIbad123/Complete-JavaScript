 //  Strict Equality(===) operator...

 //  It checks whether its two operands are equal, returning Boolean Value.
 //  It also checks or validates data types of operands.


 //  Example:

 console.log("hello"==="hello");  //  true
 console.log("3"===3);            //  false


 //  Strict InEquality(!==) operator is same as the Strict Equality operator, difference is that it reverses the result of Strict Equality Operator.