 //  Dynamic Typing...

 //  JavaScript is a Dynamic language with dynamic types. Variables in JS are not directly associated with any particular value type, and any  variable can be assigned and (re-assigned) values of all types.


 // Example:

 let foo = 42; // foo is now a number...
 foo = "bar";  // foo is now a string...
 foo = true;   // foo is now a boolean...
 console.log(foo);

 //  Output:
 //  true

